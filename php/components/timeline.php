            <p>DLSC and the Christian Brothers were allowed to continue teaching in the midst of World War II. They opened to teach students from other schools that closed down, even while Japanese forces occupied a part of the campus as their South Manila defense quarters. The college discontinued classes in 1943 and transferred students to St. Scholastica’s College.</p>
         </div>
