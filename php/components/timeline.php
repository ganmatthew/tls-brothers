            <p>De La Salle College transferred from Paco to its current location in Taft Avenue, Malate due to the rising student population. Throughout the construction of the new building, classes were being conducted in it already. Besides primary and secondary levels, tertiary education was in the works through a two-year Commerce program. </p>
         </div>
      </div>
